// src: https://futurestud.io/tutorials/generate-a-random-number-in-range-with-javascript-node-js
function randInt(min, max) {
  return Math.floor(Math.random() * (max - min) + min)
}

// https://stackoverflow.com/a/5915122
function randEl(arr: string[]) {
  return arr[Math.floor(Math.random() * arr.length)]
}

export default function generateRandomSlug() {
  return `${randEl(encouragements)}-${randEl(animals)}-${randInt(100, 999)}`
}
